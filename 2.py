

idade = int(input('Digite sua idade: '))

mes = idade * 12

dia = idade * 365

hora = dia * 24

minuto = hora * 60

segundos = minuto * 60

print('\nVocê tem',idade, 'anos que convertido são',mes, 'meses, convertendo novamente são',dia, 'dias, ou',hora, 'horas, ou ',minuto, 'munitos ou',segundos, 'segundos')
